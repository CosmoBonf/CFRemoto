// Zoom and viewport control
document.addEventListener("DOMContentLoaded", function() {
    // Prevent zoom on double tap for iOS
    let lastTouchEnd = 0;
    document.addEventListener("touchend", function (event) {
        const now = (new Date()).getTime();
        if (now - lastTouchEnd <= 300) {
            event.preventDefault();
        }
        lastTouchEnd = now;
    }, false);

    // Detect and fix viewport issues
    function fixViewport() {
        const viewport = document.querySelector("meta[name=viewport]");
        if (viewport) {
            viewport.setAttribute("content", "width=device-width, initial-scale=1.0, maximum-scale=1.0, user-scalable=no, shrink-to-fit=no");
        }
    }

    // Fix viewport on orientation change
    window.addEventListener("orientationchange", function() {
        setTimeout(fixViewport, 500);
    });

    // Initial viewport fix
    fixViewport();

    // Prevent zoom on form inputs (iOS Safari)
    const inputs = document.querySelectorAll("input, textarea, select");
    inputs.forEach(input => {
        input.addEventListener("focus", function() {
            if (window.innerWidth < 768) {
                document.querySelector("meta[name=viewport]").setAttribute("content", "width=device-width, initial-scale=1.0, maximum-scale=1.0, user-scalable=no");
            }
        });
        
        input.addEventListener("blur", function() {
            document.querySelector("meta[name=viewport]").setAttribute("content", "width=device-width, initial-scale=1.0, maximum-scale=1.0, user-scalable=no, shrink-to-fit=no");
        });
    });
});


